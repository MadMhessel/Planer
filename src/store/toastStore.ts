import { create } from 'zustand';

export type ТипУведомления = 'успех' | 'ошибка' | 'предупреждение' | 'инфо';

export interface Уведомление {
  id: string;
  тип: ТипУведомления;
  заголовок: string;
  описание?: string;
  длительность: number;
}

interface ПоказатьУведомлениеПараметры {
  тип?: ТипУведомления;
  заголовок: string;
  описание?: string;
  длительность?: number;
}

interface ToastState {
  уведомления: Уведомление[];
  показатьУведомление: (параметры: ПоказатьУведомлениеПараметры) => void;
  убратьУведомление: (id: string) => void;
  очиститьУведомления: () => void;
}

const длительностиПоТипам: Record<ТипУведомления, number> = {
  успех: 3500,
  ошибка: 4500,
  предупреждение: 5000,
  инфо: 4000,
};

const создатьId = () => {
  if (typeof crypto !== 'undefined' && 'randomUUID' in crypto) {
    return crypto.randomUUID();
  }

  return `${Date.now()}-${Math.random().toString(36).slice(2, 9)}`;
};

export const useToastStore = create<ToastState>((set, get) => ({
  уведомления: [],
  показатьУведомление: ({ тип = 'инфо', заголовок, описание, длительность }) => {
    const id = создатьId();
    const длительностьУведомления = длительность ?? длительностиПоТипам[тип];

    set((state) => ({
      уведомления: [...state.уведомления, { id, тип, заголовок, описание, длительность: длительностьУведомления }],
    }));

    window.setTimeout(() => {
      get().убратьУведомление(id);
    }, длительностьУведомления);
  },
  убратьУведомление: (id) =>
    set((state) => ({
      уведомления: state.уведомления.filter((уведомление) => уведомление.id !== id),
    })),
  очиститьУведомления: () => set({ уведомления: [] }),
}));
